package com.wulitao.netease.entity;


public class Url {

	private int br;
	
	private boolean canExtend;
	
	private int code;
	
	private int expi;
	
	private int fee;
	
	private int flag;
	
	private double gain;
	
	private int id;
	
	private String md5;
	
	private int payed;
	
	private int size;
	
	private String type;
	
	private String url;

	public int getBr() {
		return br;
	}

	public void setBr(int br) {
		this.br = br;
	}

	public boolean isCanExtend() {
		return canExtend;
	}

	public void setCanExtend(boolean canExtend) {
		this.canExtend = canExtend;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public int getExpi() {
		return expi;
	}

	public void setExpi(int expi) {
		this.expi = expi;
	}

	public int getFee() {
		return fee;
	}

	public void setFee(int fee) {
		this.fee = fee;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public double getGain() {
		return gain;
	}

	public void setGain(double gain) {
		this.gain = gain;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMd5() {
		return md5;
	}

	public void setMd5(String md5) {
		this.md5 = md5;
	}

	public int getPayed() {
		return payed;
	}

	public void setPayed(int payed) {
		this.payed = payed;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
}

